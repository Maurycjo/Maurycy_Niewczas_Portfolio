package pl.pwr.edu.music;

public class ArtistFromFile {


    private String artistName;
    private String url;

    public String getArtistName() {
        return artistName;
    }

    public String getUrl() {
        return url;
    }

    public ArtistFromFile(String artistName, String url) {

        this.artistName=artistName;
        this.url=url;


    }


}
